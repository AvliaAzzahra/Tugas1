package mtk;
public class MTK {
    public static void main(String[] args) {
    }

    private int a;
    private int b;

    public void setA(int a) {
        this.a = a;
    }
    public void setB(int b) {
        this.b = b;
    }
    public int getA() {
        return a;
    }
    public int getB() {
        return b;
    }
    public int tambah() {
        return this.a + this.b;
    }
    public int kali() {
        return this.a * this.b;
    }
}

